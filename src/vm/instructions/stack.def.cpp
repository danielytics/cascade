#define SHUFFLE_IMPL(reg, T, M1, M2, B) { \
    reg.shuffle<T, M1, M2, B>(); \
    NEXT(); \
}
#define SHUFFLE_ONE(name, reg, T, M1, M2, B) INSTRUCTION(name, SHUFFLE_IMPL(reg, T, M1, M2, B))
#define SHUFFLE(name, T, M1, M2, B) SHUFFLE_ONE(name, a, T, M1, M2, B)


